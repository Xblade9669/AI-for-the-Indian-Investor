SYSTEM_PROMPT = "You are a stock analysis agent. Follow steps: scan → context → portfolio → backtest."

def run_agent(user_query: str, chat_history: list = None, verbose: bool = True):

    # ── Build messages with memory ──────────────────────────
    messages = [{"role": "system", "content": SYSTEM_PROMPT}]

    if chat_history:
        messages.extend(chat_history)   # ✅ inject memory

    messages.append({"role": "user", "content": user_query})

    # ── Agent loop ─────────────────────────────────────────
    for step in range(10):

        response = client.chat.completions.create(
            model=GROQ_MODEL,
            messages=messages,
            tools=TOOLS,
            tool_choice="auto",
            temperature=0.1,
        )

        msg = response.choices[0].message

        # ✅ Final answer (no tool call)
        if not msg.tool_calls:
            if verbose:
                print(f"\n{'='*50}")
                print(f"STEP {step+1}: Final Answer")
                print(f"{'='*50}")
            return msg.content

        # ✅ IMPORTANT: Convert message properly (Groq format safe)
        messages.append({
            "role": "assistant",
            "content": msg.content or "",
            "tool_calls": [
                {
                    "id": tc.id,
                    "type": "function",
                    "function": {
                        "name": tc.function.name,
                        "arguments": tc.function.arguments
                    }
                } for tc in msg.tool_calls
            ]
        })

        # ── Execute tools ───────────────────────────────────
        for tc in msg.tool_calls:
            tool_name = tc.function.name

            try:
                args = json.loads(tc.function.arguments)
            except:
                args = {}

            if verbose:
                print(f"\n⚙️ STEP {step+1} | Tool: {tool_name}")
                print(f"Args: {args}")

            result = execute_tool(tool_name, args)

            if verbose:
                print(f"Result preview: {result[:200]}")

            messages.append({
                "role": "tool",
                "tool_call_id": tc.id,
                "content": result
            })

    return "⚠️ Max steps reached"


