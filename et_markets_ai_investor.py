# ── 4A: Compute all indicators ─────────────────────────────

def compute_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add EMA, RSI, MACD, Bollinger Bands, ATR to OHLCV dataframe.
    """
    df = df.copy()

    # Safety check
    required_cols = {"open", "high", "low", "close", "volume"}
    if not required_cols.issubset(df.columns):
        raise ValueError(f"Missing required columns: {required_cols - set(df.columns)}")

    close = df["close"]
    high  = df["high"]
    low   = df["low"]

    # Trend
    df["ema20"]     = ta.trend.ema_indicator(close, window=20)
    df["ema50"]     = ta.trend.ema_indicator(close, window=50)
    df["ema200"]    = ta.trend.ema_indicator(close, window=200)
    df["macd"]      = ta.trend.macd(close)
    df["macd_sig"]  = ta.trend.macd_signal(close)
    df["macd_diff"] = ta.trend.macd_diff(close)

    # Momentum
    df["rsi"]       = ta.momentum.rsi(close, window=14)
    df["stoch_k"]   = ta.momentum.stoch(high, low, close, window=14, smooth_window=3)

    # Volatility
    df["bb_upper"]  = ta.volatility.bollinger_hband(close, window=20)
    df["bb_lower"]  = ta.volatility.bollinger_lband(close, window=20)
    df["bb_mid"]    = ta.volatility.bollinger_mavg(close, window=20)
    df["atr"]       = ta.volatility.average_true_range(high, low, close, window=14)

    # Volume
    df["vol_ma20"]  = df["volume"].rolling(20).mean()

    return df.dropna()


# ── 4B: Pattern Detection ──────────────────────────────────

def detect_patterns(df: pd.DataFrame, ticker: str) -> list:
    patterns = []

    if len(df) < 60:
        return patterns

    c  = df.iloc[-1]
    p  = df.iloc[-2]
    p5 = df.iloc[-6:-1]

    close = float(c["close"])
    atr   = float(c["atr"])

    # 1 — Golden Cross
    if float(p["ema20"]) < float(p["ema50"]) and float(c["ema20"]) >= float(c["ema50"]):
        patterns.append({
            "pattern": "Golden Cross",
            "type": "bullish",
            "conviction": 8,
            "entry": round(close, 2),
            "stop_loss": round(float(c["ema50"]) * 0.97, 2),
            "target": round(close * 1.12, 2),
            "evidence": f"EMA20 ({c['ema20']:.1f}) crossed above EMA50 ({c['ema50']:.1f})"
        })

    # 2 — RSI Bounce
    if float(p["rsi"]) < 32 and float(c["rsi"]) > 35:
        patterns.append({
            "pattern": "RSI Oversold Bounce",
            "type": "bullish",
            "conviction": 7,
            "entry": round(close, 2),
            "stop_loss": round(float(df["close"].iloc[-5:].min()) * 0.985, 2),
            "target": round(close + 3 * atr, 2),
            "evidence": f"RSI bounce {p['rsi']:.1f} → {c['rsi']:.1f}"
        })

    # 3 — Bollinger Breakout
    vol_surge = float(c["volume"]) > 1.5 * float(c["vol_ma20"])
    if float(c["close"]) > float(c["bb_upper"]) and float(p["close"]) <= float(p["bb_upper"]) and vol_surge:
        patterns.append({
            "pattern": "Bollinger Breakout + Volume",
            "type": "bullish",
            "conviction": 8,
            "entry": round(close, 2),
            "stop_loss": round(float(c["bb_upper"]) * 0.985, 2),
            "target": round(close + 4 * atr, 2),
            "evidence": "Breakout with volume confirmation"
        })

    # 4 — MACD Crossover
    if float(p["macd"]) < float(p["macd_sig"]) and float(c["macd"]) >= float(c["macd_sig"]):
        conviction = 8 if float(c["macd"]) < 0 else 6
        patterns.append({
            "pattern": "MACD Bullish Crossover",
            "type": "bullish",
            "conviction": conviction,
            "entry": round(close, 2),
            "stop_loss": round(close - 2 * atr, 2),
            "target": round(close + 4 * atr, 2),
            "evidence": "MACD crossover"
        })

    # 5 — Death Cross
    if float(p["ema20"]) > float(p["ema50"]) and float(c["ema20"]) < float(c["ema50"]):
        patterns.append({
            "pattern": "Death Cross",
            "type": "bearish",
            "conviction": 7,
            "entry": round(close, 2),
            "stop_loss": round(float(c["ema20"]) * 1.02, 2),
            "target": round(close * 0.92, 2),
            "evidence": "Bearish EMA crossover"
        })

    # 6 — RSI Overbought
    if float(c["rsi"]) > 75:
        patterns.append({
            "pattern": "RSI Overbought Warning",
            "type": "bearish",
            "conviction": 5,
            "entry": round(close, 2),
            "stop_loss": round(float(c["bb_upper"]) * 1.01, 2),
            "target": round(float(c["bb_mid"]), 2),
            "evidence": f"RSI high ({c['rsi']:.1f})"
        })

    return patterns


# ── 4C: Backtesting ───────────────────────────────────────

def backtest_pattern(df: pd.DataFrame, pattern_name: str, forward_days: int = 20) -> dict:
    close = df["close"].values
    rsi   = df.get("rsi", pd.Series(np.zeros(len(close)))).values
    ema20 = df.get("ema20", pd.Series(close)).values
    ema50 = df.get("ema50", pd.Series(close)).values
    macd  = df.get("macd", pd.Series(np.zeros(len(close)))).values
    macds = df.get("macd_sig", pd.Series(np.zeros(len(close)))).values

    wins = losses = total = 0

    for i in range(1, len(close) - forward_days):
        trigger = False

        if pattern_name == "RSI Oversold Bounce":
            trigger = rsi[i-1] < 33 and rsi[i] > 35
        elif pattern_name == "Golden Cross":
            trigger = ema20[i-1] < ema50[i-1] and ema20[i] >= ema50[i]
        elif pattern_name == "MACD Bullish Crossover":
            trigger = macd[i-1] < macds[i-1] and macd[i] >= macds[i]
        elif pattern_name == "Death Cross":
            trigger = ema20[i-1] > ema50[i-1] and ema20[i] < ema50[i]

        if trigger:
            fwd = (close[i + forward_days] - close[i]) / close[i]
            total += 1
            if fwd > 0.04:
                wins += 1
            else:
                losses += 1

    win_rate = round((wins / total) * 100, 1) if total >= 5 else None

    return {
        "pattern": pattern_name,
        "instances_found": total,
        "win_rate_pct": win_rate,
        "forward_days": forward_days,
        "verdict": f"{win_rate}% win rate over {total} trades" if win_rate else "Insufficient data"
    }


